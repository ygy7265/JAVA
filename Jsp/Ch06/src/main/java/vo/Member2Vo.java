package vo;

public class Member2Vo {
private String id;
private String name;
private int age;
private String hp;
private String pos;
private int dep;
private String rdate;
private int gender;
public String getId() {
	return id;
}
public void setId(String id) {
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getHp() {
	return hp;
}
public void setHp(String hp) {
	this.hp = hp;
}
public String getPos() {
	return pos;
}
public void setPos(String pos) {
	this.pos = pos;
}
public int getDep() {
	return dep;
}
public void setDep(int dep) {
	this.dep = dep;
}
public String getRdate() {
	return rdate;
}
public void setRdate(String rdate) {
	this.rdate = rdate;
}
public int getAge() {
	return age;
}
public void setAge(int age) {
	this.age = age;
}
public int getGender() {
	return gender;
}
public void setGender(int gender) {
	this.gender = gender;
}

}
